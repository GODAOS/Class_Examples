public class Operator09 {
	public static void main(String[] args) {
		// 2진수 : 0b
		// 8진수 : 0
		// 16진수 : 0x
		int a = 11; 	// 10진수
		System.out.println(a);
		
		int b = 0b11;	// 2진수
		System.out.println(b); // 3
		
		int c = 011;	// 8진수
		System.out.println(c); // 9
		
		int d = 0x11;	// 16진수
		System.out.println(d); // 17
	} // main()
} // class